#Este proceso calcula el área  de un triángulo
input("Hola, bienvenido para Calcular el área de un triángulo. Presiona enter para empezar")
#defino Base y Altura
Base=float
Altura=float
#defino resultado como real
área=float
#Los datos se lo pido al usuario
Base=int(input("Introduce Base: "))
Altura=int(input("Introduce Altura: "))
#formula para sacar el área de un triángulo es area=(base*altura)/2
área= (Base*Altura)/2
#se escribe el resultado
print("El área del triángulo es: ", área)


